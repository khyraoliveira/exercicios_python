# a Tupla é 'irmã' da lista
# a diferença entre elas é sutil: as tuplas são IMUTÁVEIS!
# enquanto as listas são mutáveis.
# na TUPLA tem que finalizar com uma vírgula ','

frutas = (
    "laranja",
    "pera",
    "uva",
)
print(frutas)

letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

pais = ("Brasil",)
print(pais)
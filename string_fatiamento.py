nome = "Khyra de Oliveira"

print(nome[0])
# primeiro caractere da string

print(nome[:5])
# se não colocar o START, ele inicia do 0 e vai até a posição 5

print(nome[10:])
# pega da posição 10 e vai até o final da string

print(nome[6:17])
# parte da posição 6 e vai até a posição 17

print(nome[0:5:2])
# ele vai pular as letras de 2 em 2

print(nome[:])
# retorna uma cópia da string

print(nome[:: -1])
# retorna a string ao contrário, espelhada
# sem start, sem stop e ao contrário
# se colocar INT, a variável só mostrará o PRIMEIRO número: nesse caso '1'
print(int(1.024122412))

# se colocar FLOAT, aí mostrará os números depois do '.' ponto
print(float(1.024122412))

# agora para mostrar o tipo da variável:
print(type(10)) # aqui é tipo 'int'
print(type(str(10))) # aqui foi convertido para string, logo é tipo string.
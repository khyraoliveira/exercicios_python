# Atribuição Simples
saldo = 500
saldo += 200 # é o mesmo que ser 'saldo = saldo + 200'.
print(saldo)

saldo = 500
saldo -= 100 # é o mesmo que ser 'saldo = saldo - 100'.
print(saldo)

saldo = 500
saldo /= 5 # é o mesmo que ser 'saldo = saldo / 5'.
print(saldo)

saldo = 500
saldo //= 5 # é o mesmo que ser 'saldo = saldo / 5'.
print(saldo)

saldo = 500
saldo %= 480 # é o mesmo que ser 'saldo = saldo / 480, daí pega o RESTO dessa divisão'.
print(saldo)

saldo = 80
saldo **= 2 # é o mesmo que ser 'saldo = saldo ** 2'.
print(saldo)